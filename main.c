int main(void)
{
	

	
	/* Backlight pin PL3, set as output, set high for 100% output */
	DDRB |= (1<<PB2);
	//PORTB |= (1<<PB2);
	PORTB &= ~(1<<PB2);
	
	
	DDRC |=(1<<PC3); 	//Ausgang LED
	PORTC |= (1<<PC3);	//Led ein
	
	
	DDRD &= ~((1<<PD6) | (1<<PD2) | (1<<PD5)); 	//Taster 1-3
	PORTD |= ((1<<PD6) | (1<<PD2) | (1<<PD5)); 	//PUllups für Taster einschalten
	
	DDRD &= ~(1<<PD4); //T0 Counter Input
	TCCR0B |= (1<<CS02) | (1<<CS01) | (1<<CS00);//Counter 0 enabled clock on rising edge
	
	//Timer 1 Configuration
	OCR1A = 0x009C;	//OCR1A = 0x3D08;==1sec
	
    TCCR1B |= (1 << WGM12);
    // Mode 4, CTC on OCR1A

    TIMSK1 |= (1 << OCIE1A);
    //Set interrupt on compare match

    TCCR1B |= (1 << CS12) | (1 << CS10);
    // set prescaler to 1024 and start the timer

    sei();
    // enable interrupts
	
	setup();
	
	zaehler=222;
	entprell=0;
	test1=0;
	test2=0;
	ms10=0;
	ms100=0;
	sec=0;
	min=0;
	
	
		glcd_tiny_set_font(Font5x7,5,7,32,127);
		glcd_clear_buffer();
	
	
	/*	
		EEWrite_8(1, 1, 5);
		test1=55;
		test1=EERead_8(1, 1);
		*/
		
		
		
		
		
		
		glcd_write();
		
		while(1) 
		{
		
		
			sprintf(string,"%01d",sec);
			glcd_draw_string_xy(0,0,string);
		
		
		
		if(T_RED)
		{
			LED_AUS;
		}else
		{
			LED_EIN;
		}
		

	glcd_write();
	}//End of while
	
	return 0;
}//end of main
